package sintaxe_variaveis_e_fluxo;

public class TesteWhile {
	
	public static void main(String[] args) {
	
		int count = 0;
		
		while (count < 10) {
			System.out.println(count);
			// CONTADOR = CONTADOR +!;
			// CONTADOR += 1;
			count++;
			
		}
		
		System.out.println(count);
	}

}
